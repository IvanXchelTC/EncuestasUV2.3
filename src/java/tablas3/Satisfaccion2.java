
package tablas3;


public class Satisfaccion2 {
       private String id;
   private String satisfacion;
   private String total2;

    public Satisfaccion2(String id, String satisfacion, String total2) {
        this.id = id;
        this.satisfacion = satisfacion;
        this.total2 = total2;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the satisfacion
     */
    public String getSatisfacion() {
        return satisfacion;
    }

    /**
     * @param satisfacion the satisfacion to set
     */
    public void setSatisfacion(String satisfacion) {
        this.satisfacion = satisfacion;
    }

    /**
     * @return the total2
     */
    public String getTotal2() {
        return total2;
    }

    /**
     * @param total2 the total2 to set
     */
    public void setTotal2(String total2) {
        this.total2 = total2;
    }

}
